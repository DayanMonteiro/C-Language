#include <stdio.h>

int main() {

    // variável de tipo inteiro
    int minhaIdade;
    minhaIdade = 35;

    int idadeDaOutraPessoa;

    idadeDaOutraPessoa = 30;
    // %i = referencia que receberá uma variável do tipo int
    printf("Minha idade é  %i.\n", minhaIdade);
    printf("A idade da outra pessoa é %i.\n", idadeDaOutraPessoa);

    return 0;
}